    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (checkDistr.isSelected()) {
            origen = Integer.parseInt(cbxOrigen.getSelectedItem().toString());
            try {
                ListaEnlazada<Integer> lista = ca.getGnde().Dijkstra(origen);
                lista.imprimir();
                String[] aux = new String[lista.getSize()];
                for (int i = 0; i < lista.getSize(); i++) {
                    aux[i] = ca.getGnde().obtenerEtiqueta(lista.obtener(i)-1).toString();
                }
                txtcamino.setListData(aux);
            } catch (Exception ex) {
                Logger.getLogger(FrmHechos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

