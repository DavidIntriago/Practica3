    public void cargarTabla() {
        if (ca.getListaHechos() != null) {
            modeloTablaHechos.setListaHechos(ca.getListaHechos());
            tablaDocumentos.setModel(modeloTablaHechos);
            tablaDocumentos.updateUI();

        }
    }

    public void cargarCombo() throws Exception {
        Util.cargarComboActividades(cbxOrigen, ca.getListaHechos());
        Util.cargarComboActividades(cbxDestino, ca.getListaHechos());
    }

    public void cargarTablaAdyacencia() {
        try {
            modeloTablaAdyacencia.setGnde(ca.getGnde());
            tablaAdyacencia.setModel(modeloTablaAdyacencia);
            modeloTablaAdyacencia.fireTableStructureChanged();
            tablaAdyacencia.updateUI();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }

    private void fijarAdyacencia() {
        origen = Integer.parseInt(cbxOrigen.getSelectedItem().toString());
        destino = Integer.parseInt(cbxDestino.getSelectedItem().toString());

        if (cbxOrigen.getSelectedItem().equals(cbxDestino.getSelectedItem())) {
            JOptionPane.showMessageDialog(null, "No se debe escoger la misma ubicacion", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                Double tiempo = Util.calcularDiferencia(ca.getListaHechos().obtener((origen) - 1).getAnio_hecho(), ca.getListaHechos().obtener((destino) - 1).getAnio_hecho());
                ca.getGnde().insertarAristaE(ca.getListaHechos().obtener((origen) - 1), ca.getListaHechos().obtener((destino) - 1), tiempo);
                cargarTablaAdyacencia();
                ca.guardarGrafo();
            } catch (Exception e) {
            }
        }
    }

